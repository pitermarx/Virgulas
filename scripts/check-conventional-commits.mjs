import { readFile } from 'node:fs/promises'
import { spawnSync } from 'node:child_process'

const VALID_TYPES = [
    'feat',
    'fix',
    'docs',
    'style',
    'refactor',
    'perf',
    'test',
    'build',
    'ci',
    'chore',
    'revert',
]

const headerRegex = new RegExp(
    `^(?:${VALID_TYPES.join('|')})(?:\\([a-zA-Z0-9._\\/-]+\\))?(?:!)?: .+`
)

function isAutoGeneratedHeader(header) {
    return /^Merge\b/.test(header) || /^Revert\s"/.test(header)
}

function isValidHeader(header) {
    if (!header) return false
    if (isAutoGeneratedHeader(header)) return true
    return headerRegex.test(header)
}

function validationMessage(header) {
    return [
        `Invalid commit message header: "${header || '<empty>'}"`,
        'Expected Conventional Commits header: type(scope)!: subject',
        `Allowed types: ${VALID_TYPES.join(', ')}`,
        'Examples:',
        '  feat(sync): retry pull-before-push',
        '  fix(ui)!: rename storage mode label',
        '  chore: bump sw cache versions',
    ].join('\n')
}

function parseArgs(argv) {
    const args = { messageFile: '', range: '' }
    for (let i = 2; i < argv.length; i += 1) {
        const arg = argv[i]
        if (arg === '--message-file') {
            args.messageFile = argv[i + 1] || ''
            i += 1
            continue
        }
        if (arg === '--range') {
            args.range = argv[i + 1] || ''
            i += 1
            continue
        }
    }
    return args
}

async function validateMessageFile(filePath) {
    if (!filePath) {
        console.error('Missing --message-file <path>')
        process.exit(1)
    }

    const content = await readFile(filePath, 'utf8')
    const header = content
        .split(/\r?\n/)
        .map((line) => line.trim())
        .find((line) => line && !line.startsWith('#')) || ''

    if (!isValidHeader(header)) {
        console.error(validationMessage(header))
        process.exit(1)
    }
}

function listCommitSubjects(range, maxCount = 0) {
    const args = ['log', '--format=%H%x00%s']
    if (maxCount > 0) {
        args.push('-n', String(maxCount))
    }
    if (range) {
        args.push(range)
    }

    const result = spawnSync('git', args, { encoding: 'utf8' })
    if (result.status !== 0) {
        const stderr = result.stderr?.trim() || 'unknown error'
        throw new Error(`git log failed: ${stderr}`)
    }

    const lines = result.stdout
        .split(/\r?\n/)
        .map((line) => line.trim())
        .filter(Boolean)

    return lines.map((line) => {
        const separator = line.indexOf('\u0000')
        if (separator === -1) {
            return { sha: '', subject: line }
        }
        return {
            sha: line.slice(0, separator),
            subject: line.slice(separator + 1),
        }
    })
}

function validateRange(range) {
    const commits = listCommitSubjects(range)
    const invalid = commits.filter((commit) => !isValidHeader(commit.subject))

    if (invalid.length === 0) {
        return
    }

    console.error('Conventional Commits validation failed for the following commit(s):')
    for (const commit of invalid) {
        const shortSha = commit.sha ? commit.sha.slice(0, 7) : 'unknown'
        console.error(`- ${shortSha} ${commit.subject}`)
    }
    console.error('')
    console.error('Expected format: type(scope)!: subject')
    console.error(`Allowed types: ${VALID_TYPES.join(', ')}`)
    process.exit(1)
}

function validateLatestCommit() {
    const commits = listCommitSubjects('HEAD', 1)
    const latest = commits[0]
    if (!latest) return
    if (isValidHeader(latest.subject)) return

    console.error(validationMessage(latest.subject))
    process.exit(1)
}

async function main() {
    const args = parseArgs(process.argv)

    if (args.messageFile) {
        await validateMessageFile(args.messageFile)
        return
    }

    if (args.range) {
        validateRange(args.range)
        return
    }

    validateLatestCommit()
}

main().catch((error) => {
    console.error(error.message || error)
    process.exit(1)
})
